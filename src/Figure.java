public class Figure {
    private int width;
    private int length;
    private int area;

    public int getWidth(){
        return this.width;
    }
    public void setWidth(int width){
        this.width = width;
    }
    public int getLength(){
        return this.length;
    }
    public void setLength(int length){
        this.length = length;
    }
    public int getArea(){
        return this.area;

    }
    public void setArea(int area){
        this.area = area;
    }
}
